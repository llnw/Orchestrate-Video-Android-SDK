package com.limelight.videosdk.model;

/**
 * The types of media.
 */
public enum MediaType {

    /**
     * No type is associated with the media.
     */
    LVKMediaTypeNone,
    /**
     * The media is video.
     */
    LVKMediaTypeVideo,
    /**
     * The media is audio.
     */
    LVKMediaTypeAudio,
    /**
     * The media is a live stream.
     */
    LVKMediaTypeLiveStream
}